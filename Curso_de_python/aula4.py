# Tipos int e Float
# Tipo int -> Define uma variável que armazena um valor inteiro
# Tipo float -> Define uma variável que armazena um valor decimal
# Ambos int e float são "Tipos primitivos" (built-in types) em python
# Iportante: Sempre quando for definir números decimais, é necessário utilizar o ponto como separador decimal

# A função "type" pode ser usada para que o tipo de uma variável seja retornada

numero = 1.1

print(type(numero))
print(type(1.2))
print(type("numero"))