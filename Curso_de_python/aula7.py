# Variáveis são usadas para armazenar algo em um local específico da memória do computador
# pep8: Inicie variáveis com letras minúsculas, pode usar números e underline
# O sinal de igual (=) serve para atribuir um valor a uma variável

nome_completo = "Danthe Nunes"

# Também é possível atribuir uma expressão à uma variável
soma_dois_mais_dois = 2 + 2

print(nome_completo, soma_dois_mais_dois)

# Sempre dê nomes que descrevem muito bem o que sua variável tem armazenado para que possíveis confusões não ocorram

idade = 18
maior_de_idade = idade >= 18

print("\nMaior de idade? ", maior_de_idade, sep = "")
