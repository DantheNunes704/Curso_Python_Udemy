# operadores matemáticos empython

# soma
print(9 + 9);

# subtração

print(8-8);

# divisão (sempre retornará um número float em python)
print(30 / 3);

# divisão inteira: (não mostra casas decimais, mesmo se o resultado da conta não for inteiro.)
# caso sejam passados dois inteiros em que o resultado também seja um inteiro, o output será inteiro também
# (sem casas decimais)
print(4 // 2);

# esponenciação (o primeiro elevado ao segundo)
print(2 ** 3);

# resto de divisão (retorna o resto de uma divisão)
print(25 * 5);